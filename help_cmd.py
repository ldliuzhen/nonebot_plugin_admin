CATEGORY_ALIASES = {}
for _key, _data in HELP_CATEGORIES.items():
    CATEGORY_ALIASES[_key] = _key
    CATEGORY_ALIASES[_data['name']] = _key

help_cmd = on_command('帮助', priority=1, aliases={'help'}, block=True)


@help_cmd.handle()
async def _(matcher: Matcher, args: Message = CommandArg()):
    query = str(args).strip()

    if not query:
        lines = ['【群管助手 — 功能菜单】\n']
        for data in HELP_CATEGORIES.values():
            lines.append(f'  [{data["name"]}] {data["desc"]}')
        lines.append(f'\n输入 /帮助 [类别名] 查看详细指令')
        lines.append(f'例如：/帮助 群管  或  /帮助 审批')
        await matcher.finish('\n'.join(lines))
    else:
        cat_key = CATEGORY_ALIASES.get(query)
        if cat_key and cat_key in HELP_CATEGORIES:
            data = HELP_CATEGORIES[cat_key]
            lines = [f'【{data["name"]}】{data["desc"]}\n']
            for cmd in data['commands']:
                lines.append(f'  {cmd}')
            await matcher.finish('\n'.join(lines))
        else:
            names = [d['name'] for d in HELP_CATEGORIES.values()]
            await matcher.finish(f'未找到「{query}」相关帮助\n可用类别：{", ".join(names)}')
